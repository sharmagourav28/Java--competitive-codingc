public class ifelse {
    public static void main() {
        boolean isSunUp = true;
        if (isSunUp) {
            System.out.println("Day");
        } else {
            System.out.println("Night");
        }

        int age = 19;
        if (age >= 18) {
            System.out.println("Vote");

        } else {
            System.out.println("Not vote");
        }
    }
}